package com.basics.operators;

public class ComparisonOperators {
    public static void main(String[] args) {
        int firstNumber=10;
        int secondNumber=20;

        if(firstNumber==secondNumber)
            System.out.println("First number == Second Number");

        if(firstNumber!=secondNumber)
            System.out.println("First number != Second Number");

        if(firstNumber>secondNumber)
            System.out.println("First number > Second Number");

        if(firstNumber<secondNumber)
            System.out.println("First number < Second Number");

        if(firstNumber<=secondNumber)
            System.out.println("First number <= Second Number");

    }
}
