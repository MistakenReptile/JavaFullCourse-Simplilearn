package com.basics.operators;

public class ConditionalOperators {
    public static void main(String[] args) {
        int firstNumber=10;
        int secondNumber=20;

        if((firstNumber==10)&&(secondNumber==20))
            System.out.println("First number is 10 AND Second Number is 20");

        if((firstNumber==10)||(secondNumber==20))
            System.out.println("First number is 10 OR Second Number is 20");
    }
}
